class calculator:
    def addition(x,y):
        added = x + y
        print(added)
    def substraction(x,y):
        sub = x - y
        print(sub)
    def multiplication(x,y):
        mult = x * y
        print(mult)
    def division(x,y):
        div = x / y
        print(div)

calculator.addition(4,6)
calculator.substraction(4,6)
calculator.multiplication(4,6)
calculator.division(4,6)
